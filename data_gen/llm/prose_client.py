def _generate_stub(
    rendered_system_prompt: str,
    output_schema: Type[T],
    prompt_hash: str,
) -> T:
    """
    Generate deterministic template-based prose (no LLM call).

    Uses the prompt_hash to select consistently from pre-written stubs.
    All stubs pass the D-02 fence (no dollar amounts, no YYYY-MM-DD dates).

    This fallback is used when no valid API key is available, per the plan
    execution environment note.
    """
    schema_name = output_schema.__name__
    # Use first 4 hex chars of hash as index selector (0-15 → mod pool size)
    idx = int(prompt_hash[:4], 16)

    if schema_name == "SlackMessageProse":
        if "noise" in rendered_system_prompt.lower() and "signal" not in rendered_system_prompt[:200].lower():
            stubs = _SLACK_NOISE_STUBS
        else:
            stubs = _SLACK_SIGNAL_STUBS
        return output_schema(
            opening_line=stubs["opening_line"][idx % len(stubs["opening_line"])],
            body_paragraph=stubs["body_paragraph"][idx % len(stubs["body_paragraph"])],
            closing_line=stubs["closing_line"][idx % len(stubs["closing_line"])],
        )

    if schema_name == "EmailProse":
        if "noise" in rendered_system_prompt.lower() and "signal" not in rendered_system_prompt[:200].lower():
            stubs = _EMAIL_NOISE_STUBS
        else:
            stubs = _EMAIL_SIGNAL_STUBS
        return output_schema(
            subject=stubs["subject"][idx % len(stubs["subject"])],
            greeting=stubs["greeting"][idx % len(stubs["greeting"])],
            body_paragraphs=stubs["body_paragraphs"][idx % len(stubs["body_paragraphs"])],
            closing=stubs["closing"][idx % len(stubs["closing"])],
        )

    if schema_name == "DocsProse":
        if "noise" in rendered_system_prompt.lower() and "signal" not in rendered_system_prompt[:200].lower():
            stubs = _DOCS_NOISE_STUBS
        else:
            stubs = _DOCS_SIGNAL_STUBS
        return output_schema(
            title=stubs["title"][idx % len(stubs["title"])],
            executive_summary=stubs["executive_summary"][idx % len(stubs["executive_summary"])],
            body_sections=stubs["body_sections"][idx % len(stubs["body_sections"])],
            action_items=stubs["action_items"][idx % len(stubs["action_items"])],
        )

    if schema_name == "NpsVerbatimProse":
        verbatims = [
            "The platform has been reliable and our team finds it valuable for our use cases overall.",
            "Support response times have improved and the product continues to meet our core needs adequately.",
            "We have had concerns raised by our engineering team about some aspects of the product experience.",
            "The platform capabilities are strong but we feel the partnership could be more proactive.",
            "Our team has appreciated the technical depth of the product and the responsiveness of support.",
        ]
        return output_schema(
            verbatim_text=verbatims[idx % len(verbatims)],
        )

    if schema_name == "PdfSectionProse":
        if "noise" in rendered_system_prompt.lower() and "signal" not in rendered_system_prompt[:200].lower():
            stubs = _PDF_NOISE_STUBS
        else:
            stubs = _PDF_SIGNAL_STUBS
        return output_schema(
            headline=stubs["headline"][idx % len(stubs["headline"])],
            narrative_paragraph=stubs["narrative_paragraph"][idx % len(stubs["narrative_paragraph"])],
            key_findings=stubs["key_findings"][idx % len(stubs["key_findings"])],
        )

    raise ValueError(f"No stub available for schema: {schema_name}")


