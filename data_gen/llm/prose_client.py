def _generate_with_anthropic(
    rendered_system_prompt: str,
    output_schema: Type[T],
) -> T:
    """Call Anthropic claude-haiku-4-5 with structured output."""
    import anthropic  # noqa: PLC0415 — lazy import; not always present

    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        raise ValueError("ANTHROPIC_API_KEY is not set")

    client = anthropic.Anthropic(api_key=api_key)
    response = client.messages.parse(
        model="claude-haiku-4-5",
        max_tokens=1024,
        temperature=0,
        system=rendered_system_prompt,
        messages=[{"role": "user", "content": "Fill the prose fields."}],
        output_format=output_schema,
    )
    return response.parsed_output  # type: ignore[return-value]


def _generate_with_openai(
    rendered_system_prompt: str,
    output_schema: Type[T],
) -> T:
    """Call OpenAI gpt-4o-mini with structured output via beta.chat.completions.parse."""
    from openai import OpenAI  # noqa: PLC0415 — lazy import

    api_key = os.environ.get("OPENAI_API_KEY")
    if not api_key:
        raise ValueError("OPENAI_API_KEY is not set")

    client = OpenAI(api_key=api_key)
    completion = client.beta.chat.completions.parse(
        model="gpt-4o-mini",
        temperature=0,
        max_tokens=1024,
        messages=[
            {"role": "system", "content": rendered_system_prompt},
            {"role": "user", "content": "Fill the prose fields."},
        ],
        response_format=output_schema,
    )
    result = completion.choices[0].message.parsed
    if result is None:
        raise ValueError("OpenAI returned no parsed output")
    return result  # type: ignore[return-value]


